package org.example.dao;

import org.example.entity.UserEntity;

import java.util.List;

/**
 * Data Access Object (DAO) для работы с сущностью {@link UserEntity}.
 * <p>
 * Определяет базовые CRUD-операции для взаимодействия с базой данных.
 * Реализация интерфейса отвечает за использование Hibernate и управление транзакциями.
 */
public interface UserDao {
    /**
     * Сохраняет нового пользователя в базе данных.
     *
     * @param user объект пользователя, который необходимо сохранить
     */
    void save(UserEntity user);

    /**
     * Находит пользователя по его идентификатору.
     *
     * @param id уникальный идентификатор пользователя
     * @return найденный пользователь или {@code null}, если пользователь не найден
     */
    UserEntity findById(Long id);

    /**
     * Возвращает список всех пользователей из базы данных.
     *
     * @return список пользователей; может быть пустым, если записи отсутствуют
     */
    List<UserEntity> findAll();

    /**
     * Обновляет данные существующего пользователя.
     *
     * @param user объект пользователя с обновлёнными данными
     */
    void update(UserEntity user);

    /**
     * Удаляет пользователя по его идентификатору.
     *
     * @param id уникальный идентификатор пользователя
     */
    void delete(Long id);
}